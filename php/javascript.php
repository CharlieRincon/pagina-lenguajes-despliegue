        <p class="text-justify center-block">JavaScript (abreviado comúnmente JS) es un lenguaje de programación interpretado, dialecto del
            estándar ECMAScript. Se define como orientado a objetos, basado en prototipos, imperativo,
            débilmente tipado y dinámico.<br>
            
            Se utiliza principalmente en su forma del lado del cliente (client-side), implementado como parte
            de un navegador web permitiendo mejoras en la interfaz de usuario y páginas web dinámicas aunque
            existe una forma de JavaScript del lado del servidor (Server-side JavaScript o SSJS). Su uso en
            aplicaciones externas a la web, por ejemplo en documentos PDF, aplicaciones de escritorio
            (mayoritariamente widgets) es también significativo.<br>

            Desde el 2012, todos los navegadores modernos soportan completamente ECMAScript 5.1, una versión de
            javascript. Los navegadores más antiguos soportan por lo menos ECMAScript 3. La sexta edición se
            liberó en julio del 2015.<br>

            JavaScript se diseñó con una sintaxis similar a C, aunque adopta nombres y convenciones del
            lenguaje de programación Java. Sin embargo, Java y JavaScript tienen semánticas y propósitos
            diferentes.<br>

            Todos los navegadores modernos interpretan el código JavaScript integrado en las páginas web. Para
            interactuar con una página web se provee al lenguaje JavaScript de una implementación del Document
            Object Model (DOM).<br>

            Tradicionalmente se venía utilizando en páginas web HTML para realizar operaciones y únicamente en
            el marco de la aplicación cliente, sin acceso a funciones del servidor. Actualmente es ampliamente
            utilizado para enviar y recibir información del servidor junto con ayuda de otras tecnologías como
            AJAX. JavaScript se interpreta en el agente de usuario al mismo tiempo que las sentencias van
            descargándose junto con el código HTML.<br>

            Desde el lanzamiento en junio de 1997 del estándar ECMAScript 1, han existido las versiones 2, 3 y
            5, que es la más usada actualmente (la 4 se abandonó​). En junio de 2015 se cerró y publicó la
            versión ECMAScript 6.7​<br>
